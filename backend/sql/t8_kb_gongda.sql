（一）学习成绩排名在本专业前 50%，或者取得一定的学术科研成果；

（二）勇于克服生活中的重重困难，以顽强的作风坚持学习、工作，积极参加各项社会活动，或者在某方面做出突出成绩；

（三）本奖项与学习优秀奖不重复；

（四）本科在读期间，获奖次数不超过两次。

创新创业奖

